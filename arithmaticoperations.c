//Program to show different arithmatic operations 
#include<stdio.h>
int main()
{
    int num1,num2;
    num1=10;
    num2=5;
    printf("\nNum1 = %d",num1);
    printf("\nNum2 = %d",num2);
    printf("\nThe sum of the numbers %d and %d is %d \n",num1,num2,num1+num2);
    printf("\nThe difference of the numbers %d and %d is %d \n",num1,num2,num1-num2);
    printf("\nThe multipication of the numbers %d and %d is %d \n",num1,num2,num1*num2);
    printf("\nThe division of the numbers %d and %d is %d \n",num1,num2,num1/num2);
    printf("\nThe reminder is %d \n",num1%num2);
    return 0;
}